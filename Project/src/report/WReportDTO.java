package report;

public class WReportDTO {
	private int no;
	private String reporter;
	private String suspecter;
	private String content;
	private String img;
	private String handling;
	private String askday;
	private String handleday;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getReporter() {
		return reporter;
	}
	public void setReporter(String reporter) {
		this.reporter = reporter;
	}
	public String getSuspecter() {
		return suspecter;
	}
	public void setSuspecter(String suspecter) {
		this.suspecter = suspecter;
	}
	
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getHandling() {
		return handling;
	}
	public void setHandling(String handling) {
		this.handling = handling;
	}
	public String getAskday() {
		return askday;
	}
	public void setAskday(String askday) {
		this.askday = askday;
	}
	public String getHandleday() {
		return handleday;
	}
	public void setHandleday(String handleday) {
		this.handleday = handleday;
	}
}
